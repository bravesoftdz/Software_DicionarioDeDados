    private void cmbTabelaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbTabelaItemStateChanged
        // TODO add your handling code here:
        Tabela tab = (Tabela)cmbTabela.getSelectedItem();
        jtfNomeTab.setText(tab.getNome());
        jtfTipoTab.setText(tab.getTipo());
        jtaDescricao.setText(tab.getDescricao());
        
        try {        
            control.carregarComboCampos(cmbCampos, jtfNomeCampo, jtfTipoCampo, jtaDescricaoCampo, tab);
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(this, "Erro ao consultar Bancos de Dados para Carregar ComboCampos. " + 
                    erro.getMessage() + erro.getClass() );            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, "Erro não esperado ao carregar ComboCampos. " + 
                    erro.getMessage() + erro.getClass() );
        }
        
        Campo campo = (Campo) cmbCampos.getSelectedItem();
        jtfNomeCampo.setText(campo.getNome());
        jtfTipoCampo.setText(campo.getTipo());
        jtaDescricaoCampo.setText(campo.getDescricao());
        
    }//GEN-LAST:event_cmbTabelaItemStateChanged

