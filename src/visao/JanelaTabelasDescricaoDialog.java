    private void btnSalvarTabelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarTabelaActionPerformed
        // TODO add your handling code here:
        Tabela tab = (Tabela)cmbTabela.getSelectedItem();
        tab.setDescricao(jtaDescricao.getText());
        tab.setNome(jtfNomeTab.getText());
        tab.setTipo(jtfTipoTab.getText());
        
        try {
            control.alterarTabela(tab);
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(this, "Erro ao consultar Bancos de Dados para Carregar ComboCampos. " + 
                    erro.getMessage() + erro.getClass() );            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, "Erro não esperado ao carregar ComboCampos. " + 
                    erro.getMessage() + erro.getClass() );
        }
        jtfNomeTab.setEditable(false);
        jtfTipoTab.setEditable(false);
        jtaDescricao.setEditable(false);
        btnEditarTabela.setVisible(true);
        btnSalvarTabela.setVisible(false);
        btnCancelarTabela.setVisible(false);
    }//GEN-LAST:event_btnSalvarTabelaActionPerformed

